app.post('/chat', async (req, res) => {
  const { messages } = req.body;

  if (!ANTHROPIC_API_KEY || ANTHROPIC_API_KEY === 'ТВОЯТ_API_КЛЮЧ_ТУК') {
    return res.status(500).json({ error: 'API ключът не е настроен. Отвори server.js и добави своя ключ.' });
  }

  try {
    const client = new Anthropic({ apiKey: ANTHROPIC_API_KEY });

    const response = await client.messages.create({
      model: 'claude-haiku-4-5-20251001',
      max_tokens: 1024,
      system: SYSTEM_PROMPT,
      messages: messages
    });

    res.json({ reply: response.content[0].text });
  } catch (error) {
    console.error('Грешка:', error.message);
    res.status(500).json({ error: 'Грешка при свързване с AI. Провери API ключа.' });
  }
});

// /ask endpoint — за ConvertBuilder чата (приема {question}, връща {answer})
app.post('/ask', async (req, res) => {
  const { question } = req.body;
  if (!question) return res.status(400).json({ answer: 'Няма въпрос.' });

  try {
    const client = new Anthropic({ apiKey: ANTHROPIC_API_KEY });
    const response = await client.messages.create({
      model: 'claude-haiku-4-5-20251001',
      max_tokens: 1024,
      system: SYSTEM_PROMPT,
      messages: [{ role: 'user', content: question }]
    });
    res.json({ answer: response.content[0].text });
  } catch (error) {
    console.error('Грешка /ask:', error.message);
    res.status(500).json({ answer: 'Нещо се обърка. Опитай отново.' });
  }
});

